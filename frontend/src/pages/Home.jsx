import React from 'react'
import { useEffect, useState } from 'react'
import { Loader , Card , FormField } from '../components';

const RenderCards = ({data , title} ) =>{
 if(data?.length > 0) {
    return data.map((post) => <Card key={post._id} {...post} /> )
 }

 return (
    <h2 className='mt-5 font-bold text-[#6469ff] text-xl uppercase'>
      {title}
    </h2>
 )
}

const Home = () => {
   const [loading, setloading] = useState(false);
   const [allPost, setallPost] = useState(null);
   const [searchText, setsearchText] = useState('');
   const [searchedResults, setSearchedResults] = useState(null);
   const [searchTimeout, setsearchTimeout] = useState(null);

   useEffect(() =>{
        const fetchPosts = async () =>{
           
            setloading(true);
            try {
                const response = await fetch('http://localhost:8080/api/v1/post' , {
                    method: 'GET',
                    headers: {
                        'Content-Type': 'application/json', 
                    }

                })

                if(response.ok)
                   {
                    const result = await response.json();

                    setallPost(result.data.reverse());
                   } 
            } catch (error) {
               alert(error); 
            }finally{
                setloading(false);
            }
        }

        fetchPosts();
   },[]) //only be called at start

   const handeleSearchChange = (e) =>{
    clearTimeout(setTimeout);

    setsearchText(e.target.value);

    setsearchTimeout(
     setTimeout(() =>  {
        const searchResults = allPost.filter((item) => item.name.toLowerCase().includes(searchText.toLowerCase() ) || item.prompt.toLowerCase().includes(searchText.toLowerCase()));
        setSearchedResults(searchResults);
    
    }, 500)
    );
   }

   return (
    <section className='mx-w-7xl mx-auto'>
        <div>
            <h1 className='font-extrabold text-[#222328] text-[32px]'>Community Showcase</h1>
            <p className='mt-2 text-[#666e75] text-[16px] max-w[500px]'>Browse through a collection of imaginative and visually stunning images generated by DALL-E AI</p>
        </div>

        <div className='mt-16'>
            <FormField 
            LabelName="Search posts"
            type='text'
            name='text'
            placeholder="Search Posts....!!!"
            value={searchText}
            handleChange={handeleSearchChange}
            />
        </div>

        <div className='mt-10'>
           { loading ? (
            <div className='flex justify-center items-center'>
                <Loader />
             </div>   
           ) : (
            <>
             {searchText && (
                <h2 className='font-medium text-[#666e75  text-xl mb-3'>
                    Showing Results for <span className='text-[#222328'>{searchText}</span>
                </h2>
             )}

             <div className='grid lg:grid-cols-4 sm:grid-cols-3 xs:grid-cols-2 grid-cols-1 gap-3'>
                {searchText ? (
                    <RenderCards 
                     data={searchedResults}
                     title="No search results found"
                    />
                ) : (
                    <RenderCards 
                     data={allPost}
                     title="No posts found"
                    />
                )}
             </div>
            </>
           )}
        </div>

    </section>

   
  )
}

export default Home